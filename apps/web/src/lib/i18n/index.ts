export type Messages = (typeof dictionaries)[Locale];

export function resolveLocale(value?: string | null): Locale {
  if (!value) return "zh-CN";
  const normalized = value.trim().toLowerCase();
  if (normalized === "zh-tw" || normalized === "zh-hk" || normalized === "zh-hant") return "zh-TW";
  if (normalized.startsWith("en")) return "en";
  return "zh-CN";
}

export function getMessages(locale: Locale): Messages {
  return dictionaries[locale];
}

export function getLocaleFontFamily(locale: Locale): string {
  if (locale === "zh-TW") {
    return '"PingFang TC", "Noto Sans CJK TC", "Microsoft JhengHei", system-ui, sans-serif';
  }
  if (locale === "zh-CN") {
    return '"PingFang SC", "Noto Sans CJK SC", "Microsoft YaHei", system-ui, sans-serif';
  }
  return 'Inter, "SF Pro Text", system-ui, sans-serif';
}
